     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $titulos = Titulo::all();

        $obj = array();
        
        foreach ($titulos as $titulo) {

            array_push( $obj,

                array(
                    "titulo" => $titulo->titulo,
                    "contenido" => array(
                        "items" => explode('.',$titulo->items),
                    )
                )
            );

        }

        $response = Response::json($obj, 200);
        return $response;
    }

